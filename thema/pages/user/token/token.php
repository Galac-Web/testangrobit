<script>
    // Получаем текущий домен страницы
    const currentDomain = window.location.origin;

    // Динамически создаем реферальную ссылку, используя данные PHP
    const referralLink = `${currentDomain}/regref?id=<?php echo $_SESSION['user_id']; ?>`;

    document.getElementById("copyButton").addEventListener("click", function() {
        // Создаем временный элемент для копирования ссылки
        const tempInput = document.createElement("input");
        tempInput.style.position = "absolute";
        tempInput.style.left = "-9999px";
        tempInput.value = referralLink;
        document.body.appendChild(tempInput);

        // Выделяем текст и копируем в буфер обмена
        tempInput.select();
        document.execCommand("copy");

        // Удаляем временный элемент
        document.body.removeChild(tempInput);

        alert("Ссылка скопирована в буфер обмена!");
    });
</script>