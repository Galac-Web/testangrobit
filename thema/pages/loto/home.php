<script>
    // Функция для запуска счетчиков
    function startCounters() {
        // Получаем все элементы с классом 'runcount'
        const counters = document.querySelectorAll('.runcount');

        counters.forEach(counter => {
            // Извлекаем максимальное значение и окончание из атрибутов
            const targetValue = parseInt(counter.getAttribute('data-max'), 10);
            const endName = counter.getAttribute('data-endname') || ''; // Префикс и/или суффикс
            let currentValue = 0;
            const duration = 2000; // Продолжительность анимации в миллисекундах
            const stepTime = 10; // Время между шагами в миллисекундах
            const stepValue = targetValue / (duration / stepTime);

            function updateCounter() {
                if (currentValue < targetValue) {
                    currentValue += stepValue;
                    counter.innerText = Math.ceil(currentValue).toLocaleString();
                    if (endName) {
                        counter.innerText += endName;
                    }
                    setTimeout(updateCounter, stepTime);
                } else {
                    counter.innerText = targetValue.toLocaleString();
                    if (endName) {
                        counter.innerText += endName;
                    }
                }
            }

            updateCounter();
        });
    }

    // Запуск счетчиков при загрузке страницы
    window.onload = startCounters;
</script>